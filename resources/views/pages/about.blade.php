            <p class="lead">Williams Funeral Home, Inc. was initially established in 1945 under the name of Tribe Union Funeral Home an organization formed by members of North Sumter to dispose of their own dead. It was lead by the Reverend John Wesley Williams, Sr. Founding members were: Rev. John W. Williams, Jr., Rev. Robert Anderson, Rev. Richard Lloyd, Rev. R. M. Davis, Rev. Gable Tillman, Rev. M. J. Kirk, Dea. J. C. Capers, Rev. Frank E. Williams, Sr., Mr. Robert Anderson, Mrs. Addie Martin, Ms. Jenett Garner, Mr. Murry Pinkney, Sr., “Father of Bill Pinkney the Original Drifter”.</p>

            <p class="lead">The organization under went an official name change to Williams Funeral Home in 1947 under Rev. John Wesley Williams, Sr., Rev. Frank E. Williams, Sr., Mrs. Clara P. Williams, Mrs. Annie M. Murray, Mrs. Katie S. Pinkney, Dr. L. W. Williams, Rev. Waddell A. Mack and Rev. Richard Holliday.</p>

            <p class="lead">At that time, the two story structure housed the Funeral Home on the lower level and living quarters on the upper level, making full use of all five bedrooms. Students from Morris College bordered here for many years. Williams Funeral Home became the first Black Business to join the Sumter Chamber of Commerce in 1970.</p>

            <p class="lead">Rev. John W. Williams, Jr., second eldest son of Rev. John W. Williams, Sr., a graduate of Morris College, after having served his county in the United States Army as a Chaplain, ranking Sergeant Major, returned to the aid of his father, becoming Manager, Director and Embalmer in 1948.</p>

            <p class="lead">The eldest son, Rev. Dr. George Washington Williams, Sr., a graduate of Claflin College, “University”, serving in the United States Army at that time as an Infantry Division Chaplain, ranking Lt. Col., committed moral and financial support to this endeavor while continuing to pursue his military career.</p>

            <p class="lead">The younger brother, Rev. Frank E. Williams, Sr., was trained by his brother, Rev. John W. Williams, Jr., and obtained licensing in 1955. The untimely death of Rev. John W. Williams, Jr., in 1951, left Rev. Frank E. Williams, Sr., the younger son, to shoulder the load, while Rev. John W. Williams, Sr., continued his ministry in the Methodist Church with between 55 and 60 years of service.</p>

            <p class="lead">Rev. Frank E. Williams, Sr., married Magnolia Robinson of Rembert, S. C., daughter of Deacon David Robinson and Sarah Dow Robinson in 1950. In 1966, Magnolia obtained licensing as a Funeral Director and Notary, so that she could be of greater assistance as all round lady attendant. To their union four children were born: Elder Georgia Donnell Williams-Miller, Reverend Frank E. Williams, Jr., Reverend J. Elbert Williams, and Mrs. Sandra Williams-Brailsford.</p>

            <p class="lead">Frank and Magnolia, along with their four children and the support and encouragement from his father, Rev. John W. Williams, Sr., and Mother, Clara Peay Williams, set out to develop a lucrative, and viable business in the Sumter Community, which took many years of total dedication and extreme sacrifice. Most of all the grace and blessings of God Almighty.</p>

            <p class="lead">The Home for Funerals, known today as Williams Funeral Home, Inc. is still a family owned and operated business. Reverend Frank E. Williams, Sr. died 29 July 2007. Mrs. Magnolia R. Williams is now majority owner and President of the Cooperation established in 1 August 1983. Reverend Frank E. Williams, Jr., oldest son is Vice President, Embalmer, Director and Notary, graduating from Gupton-Jones College of Mortuary Science with National Board Certification, and Benedict College, Columbia, S. C. His spouse, Thoma Lewis Williams is also an Educator, Embalmer, Notary, and Director with National Board Certification. Reverend J. Elbert Williams, the youngest son is Cooperate Secretary, Embalmer, Director and Notary, graduate of Morris College, Gupton-Jones College of Mortuary Science with National Board Certification. The youngest daughter, Mrs. Sandra Williams-Brailsford is a Certified Death Education Counselor, Cosmetologist and Notary for the State of South Carolina.</p>

            <p class="lead">Williams Funeral Home, Inc. offers a full scale conference room and arrangement room for its patrons. It also boast a beautifully designed, very spacious chapel capable of seating 250 people. The Williams brothers are the two licensed Embalmers on site daily. Their selection has a range of 25-30 caskets available at all times. The chapel is also equipped to handle 4 family viewing at one time. Renovation on the current physical structure were completed in November 1999.</p>

