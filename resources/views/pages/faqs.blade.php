            <h1 style="margin-bottom:30px;">Funeral &amp; Burial Questions</h1>
          </div>
          <div class="col-md-12 st-accordion">
            <div class="panel-group" id="accordion" role="tablist" aria-multiselectable="true">
              <div class="well-default panel panel-default">
                <div class="panel-heading" role="tab" id="headingOne">
                  <h3 class="panel-title"> <a role="button" data-toggle="collapse" data-parent="#accordion" href="#collapseOne" aria-expanded="false" aria-controls="collapseOne" class="collapsed">What purpose does a funeral serve?</a> </h3>
                </div>
                <div id="collapseOne" class="panel-collapse collapse in" role="tabpanel" aria-labelledby="headingOne" aria-expanded="false">
                  <div class="panel-body">
                    <p> The funeral ceremony is a customary way to recognize death and its finality. Funerals are recognized rituals for the living to show respect for the dead and to help survivors begin the grief process.</p>
                  </div>
                </div>
              </div>
              <div class="well-default panel panel-default">
                <div class="panel-heading active" role="tab" id="headingTwo">
                  <h3 class="panel-title"> <a class="" role="button" data-toggle="collapse" data-parent="#accordion" href="#collapseTwo" aria-expanded="true" aria-controls="collapseTwo">What do funeral directors do?</a> </h3>
                </div>
                <div id="collapseTwo" class="panel-collapse collapse" role="tabpanel" aria-labelledby="headingTwo" aria-expanded="true">
                  <div class="panel-body">
                    <p> Funeral directors are listeners, advisors and supporters, caregivers and administrators. They make the arrangements for transportation of the body, complete all necessary paperwork, and implement the choices made by the family regarding the funeral and final disposition of the body. They have experience assisting the bereaved in coping with death. Funeral directors are trained to answer questions about grief, recognize when a person is having difficulty coping, and recommend sources of professional help. Funeral directors also link survivors with support groups at the funeral home or in the community.</p>
                  </div>
                </div>
              </div>

              <!-- 3 -->
              <div class="panel panel-default well-default">
                <div class="panel-heading" role="tab" id="headingThree">
                  <h3 class="panel-title"> <a class="collapsed" role="button" data-toggle="collapse" data-parent="#accordion" href="#collapseThree" aria-expanded="false" aria-controls="collapseThree">Do you have to have a funeral director to bury the dead?</a> </h3>
                </div>
                <div id="collapseThree" class="panel-collapse collapse" role="tabpanel" aria-labelledby="headingThree" aria-expanded="false">
                  <div class="panel-body">
                    <p> In most states, family members may bury their own dead although regulations vary. However, most people find it very trying to be solely responsible for arranging the details and legal matters surrounding a death.</p>
                  </div>
                </div>
              </div>

              <!-- 4 -->
              <div class="panel panel-default well-default">
                <div class="panel-heading" role="tab" id="headingFour">
                  <h3 class="panel-title"> <a class="collapsed" role="button" data-toggle="collapse" data-parent="#accordion" href="#collapseFour" aria-expanded="false" aria-controls="collapseFour">Why have a public viewing?</a> </h3>
                </div>
                <div id="collapseFour" class="panel-collapse collapse" role="tabpanel" aria-labelledby="headingFour" aria-expanded="false">
                  <div class="panel-body">
                    <p> Viewing is part of many cultural and ethnic traditions. Many grief specialists believe that viewing aids the grief process by helping the bereaved recognize the reality of death. Viewing is encouraged for children, as long as the process is explained and the activity voluntary.</p>
                  </div>
                </div>
              </div>

              <!-- 5 -->
              <div class="panel panel-default well-default">
                <div class="panel-heading" role="tab" id="headingFive">
                  <h3 class="panel-title"> <a class="collapsed" role="button" data-toggle="collapse" data-parent="#accordion" href="#collapseFive" aria-expanded="false" aria-controls="collapseFive">What is the purpose of embalming?</a> </h3>
                </div>
                <div id="collapseFive" class="panel-collapse collapse" role="tabpanel" aria-labelledby="headingFive" aria-expanded="false">
                  <div class="panel-body">
                    <p> Embalming sanitizes and preserves the body, retards the decomposition process, and enhances the appearance of a body disfigured by traumatic death or illness. Embalming makes it possible to lengthen the time between death and the final disposition, thus allowing family members time to arrange and participate in the type of service most comforting to them.</p>
                  </div>
                </div>
              </div>

              <!-- 6 -->
              <div class="panel panel-default well-default">
                <div class="panel-heading" role="tab" id="headingSix">
                  <h3 class="panel-title"> <a class="collapsed" role="button" data-toggle="collapse" data-parent="#accordion" href="#collapseSix" aria-expanded="false" aria-controls="collapseSix">Does a dead body have to be embalmed, according to law?</a> </h3>
                </div>
                <div id="collapseSix" class="panel-collapse collapse" role="tabpanel" aria-labelledby="headingSix" aria-expanded="false">
                  <div class="panel-body">
                    <p> No. Most states, however, require embalming when death was caused by a reportable contagious disease or when remains are to be transported from one state to another by common carrier or if final disposition is not to be made within a prescribed number of hours.</p>
                  </div>
                </div>
              </div>

              <!-- 7 -->
              <div class="panel panel-default well-default">
                <div class="panel-heading" role="tab" id="headingSeven">
                  <h3 class="panel-title"> <a class="collapsed" role="button" data-toggle="collapse" data-parent="#accordion" href="#collapseSeven" aria-expanded="false" aria-controls="collapseSeven">Is cremation a substitute for a funeral?</a> </h3>
                </div>
                <div id="collapseSeven" class="panel-collapse collapse" role="tabpanel" aria-labelledby="headingSeven" aria-expanded="false">
                  <div class="panel-body">
                    <p> No, cremation is an alternative to earth burial or entombment for the body's final disposition and often follows a traditional funeral service.</p>
                  </div>
                </div>
              </div>

              <!-- 8 -->
              <div class="panel panel-default well-default">
                <div class="panel-heading" role="tab" id="headingEight">
                  <h3 class="panel-title"> <a class="collapsed" role="button" data-toggle="collapse" data-parent="#accordion" href="#collapseEight" aria-expanded="false" aria-controls="collapseEight">Why are funerals so expensive?</a> </h3>
                </div>
                <div id="collapseEight" class="panel-collapse collapse" role="tabpanel" aria-labelledby="headingEight" aria-expanded="false">
                  <div class="panel-body">
                    <p> When compared to other major life cycle events, like births and weddings, funerals are not expensive. A wedding costs at least three times as much; but because it is a happy event, wedding costs are rarely criticized. A funeral home is a 24-hour, labor-intensive business, with extensive facilities (viewing rooms, chapels, limousines, hearses, etc.), these expenses must be factored into the cost of a funeral. Moreover, the cost of a funeral includes not only merchandise, like caskets, but the services of a funeral director in making arrangements; filing appropriate forms; dealing with doctors, ministers, florists, newspapers and others; and seeing to all the necessary details. Our funeral homes are independently owned and operated with a modest profit margin.</p>
                  </div>
                </div>
              </div>

