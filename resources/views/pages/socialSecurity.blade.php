            <h2>How Social Security Helps Families</h2>
            <p>Social Security survivors benefits help ease the financial burden that follows a worker's death. Almost all children under age 18 will get monthly benefits if a working parent dies. Other family members may be eligible for benefits, too. Anyone who has worked and paid Social Security (FICA) taxes has been earning survivors benefits for his or her family. The amount of work needed to pay survivors benefits depends on the worker's age at the time of death. It may be as little as 1 Â½ years for a young worker. No one needs more than 10 years.</p>

            <h2>Who Can Get Survivors Benefits</h2>
            <p>Here is a list of family members who usually can get benefits:</p>
            <ul class="listnone angle-right">
              <li>Widows and widowers age 60 or older.</li>
              <li>Widows and widowers at any age if caring for the deceased's child(ren) who are under age 16 or disabled.</li>
              <li>Divorced wives and husbands age 60 or older, if married to the deceased 10 years or more.</li>
              <li>Widows, widowers, divorced wives, and divorced husbands age 50 or older, if they are disabled.</li>
              <li>Children up to age 18.</li>
              <li>Children age 18 - 19, if they attend elementary school or high school full time.</li>
              <li>Children over age 18, if they became disabled before age 22.</li>
              <li>The deceased worker's parents age 62 or older, if they were being supported by the worker.</li>
            </ul>

            <h2>A Special One-Time Payment</h2>
            <p>In addition to the monthly benefits for family members, a one-time payment of $255 can be paid to a spouse who was living with the worker at the time of death. If there is none, it can be paid to: A spouse who is eligible for benefits. A child or children eligible for benefits. This payment can not be made if there is no eligible spouse or child. HOW TO APPLY FOR BENEFITS You can apply for benefits by telephone or by going to any Social Security office. You may need some of the documents shown in the Information Needed section below. But don't delay your application because you don't have all the information. If you don't have a document you need, Social Security can help you get it.</p>

            <h2>Information Needed</h2>
            <p>Here is the list of Information that is required:</p>
            <ul class="listnone angle-right">
              <li>Your Social Security number and the deceased worker's Social Security number.</li>
              <li>A death certificate</li>
              <li>Proof of the deceased worker's earnings for last year (W-2 forms or self-employment tax return).</li>
              <li>Your birth certificate.</li>
              <li>A marriage certificate, if you are applying for benefits as a widow, widower, divorced wife, or divorced husband.</li>
              <li>A divorce decree, if you are applying for benefits as a divorced wife or husband.</li>
              <li>Children's birth certificates and Social Security numbers, if applying for children's benefits.</li>
              <li>Your checking or savings account information, if you want direct deposit of your benefits.</li>
            </ul>
            <p>You will need to submit original documents or copies certified by the issuing office.</p>
            <p>You can mail them or bring them to the office. Social Security will make photocopies and return your documents.</p>

            <h2>For More Information</h2>
            <p>You may contact the Social Security Office to find out what benefits are available for your individual circumstances, to change records, and to stop automatic deposits.</p>
            <p>The national phone number is 1-800-772-1213. For more information, write or visit any Social Security office, or phone the toll-free number. will file Form SSA-721 which informs Social Security of a death. You need not order a certified copy of the death certificate to send to your Social Security Administration office.</p>
