        <h2 style="color:#1a4b5d;">About Pre-Planning</h2>
        <div style="background-color:#f3fcfe;padding:20px 20px 20px 30px;border:1px solid #1a4b5d;">
          <p>To help relieve their families of some of these decisions, an increasing number of people are planning their own funerals, designating their funeral preferences, and sometimes even paying for them in advance. They see funeral planning as an extension of will and estate planning. Thinking ahead can help you make informed and thoughtful decisions about funeral arrangements. It allows you to choose the specific items you want and need and compare the prices offered by several funeral providers. It also spares your survivors the stress of making these decisions under the pressure of time and strong emotions.</p>
          <p>You can make arrangements directly with a funeral establishment or through a funeral planning or memorial society, a nonprofit organization that provides information about funerals and disposition but doesn't offer funeral services. If you chose to contact such a group, recognize that while some funeral homes may include the word "society" in their names, they are nonprofit organizations.</p>
          <p>One other important consideration when planning a funeral pre-need is where the remains will be buried, entombed or scattered. In the short time between the death and burial of a loved one, many family members find themselves rushing to buy a cemetery plot or grave, often without careful thought or a personal visit to the site. That's why it is the family's best interest to buy cemetery plots before you need them.</p>
          <p>You may wish to make decisions about your arrangements in advance, but not pay for them in advance. Keep in mind that over time, prices may go up and businesses may close or change ownership. However, in some areas with increased competition, prices may go down over time. It is a good idea to review and revise your decisions every few years, and to make sure your family is aware of your wishes.</p>
          <p>Put your preferences in writing, give copies to family members and your attorney, and keep a copy in a handy place. Don't designate your preferences in your will, because a will often is not found or read until after the funeral. And avoid putting the only copy of your preferences in a safe deposit box. That is because your family may have to make arrangements on a weekend or holiday, before the box can be opened.</p>
        </div>

        <div style="border-bottom:1px solid #1a4b5d;margin:40px 0px;"></div>

        <h2>Prepaying</h2>
        <div style="background-color:#f3fcfe;padding:20px 20px 20px 30px;border:1px solid #1a4b5d;">
          <p>Millions of Americans have entered into contracts to prearrange their funerals and prepay some or all of the expenses involved. Laws of individual states govern the prepayment of funeral goods and services; various states have laws to help ensure that these advance payments are available to pay for the funeral products and services when they are needed. But protections vary widely from state to state, and some state laws offer little or no effective protection. Some state laws require the funeral home or cemetery to place a percentage of the prepayment in a state-regulated trust or to purchase a life insurance policy with the death benefits assigned to the funeral home or cemetery.</p>
          <p>If you are thinking about prepaying for funeral goods and services, it is important to consider these issues before putting down any money:</p>
          <ul class="listnone check-circle-o" style="padding-right:30px;padding-left:30px;padding-top:0px;padding-bottom:0px;">
            <li>What are you paying for? Are you buying only merchandise, like a casket and vault, or are you purchasing funeral services as well?</li>
            <li>What happens to the money you've prepaid? States have different requirements for handling funds paid for prearranged funeral services.</li>
            <li>What happens to the interest income on money that is prepaid and put into a trust account?</li>
            <li>Are you protected if the firm you dealt with goes out of business?</li>
            <li>Can you cancel the contract and get a full refund if you change your mind?</li>
            <li>What happens if you move to a different area or die while away from home? Some prepaid funeral plans can be transferred, but often at an added cost.</li>
          </ul>
          <p>Be sure to tell your family about the plans you’ve made; let them know where the documents are filed. If your family isn’t aware that you’ve made plans, your wishes may not be carried out. And if family members don’t know that you’ve prepaid the funeral costs, they could end up paying for the same arrangements.</p>
        </div>
